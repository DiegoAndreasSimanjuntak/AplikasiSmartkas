    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) TabelLaporan.getModel();
        int selectedRow = TabelLaporan.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
            return;
        }

        int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?",
            "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/smartkas", "root", "");
                PreparedStatement ps = conn.prepareStatement("DELETE FROM laporan WHERE id = ?")) {

                ps.setInt(1, id);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                model.removeRow(selectedRow);
                hitungTotal();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) TabelLaporan.getModel();
        int selectedRow = TabelLaporan.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit!");
            return;
        }

        int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        String kategoriBaru = JOptionPane.showInputDialog(this, "Masukkan kategori baru:", model.getValueAt(selectedRow, 1));
        String jenisBaru = JOptionPane.showInputDialog(this, "Masukkan jenis (Pemasukan/Pengeluaran):", model.getValueAt(selectedRow, 2));
        String nominalStr = JOptionPane.showInputDialog(this, "Masukkan nominal baru:", model.getValueAt(selectedRow, 3));
        String catatanBaru = JOptionPane.showInputDialog(this, "Masukkan catatan baru:", model.getValueAt(selectedRow, 4));
        String tanggalBaru = JOptionPane.showInputDialog(this, "Masukkan tanggal baru (YYYY-MM-DD):", model.getValueAt(selectedRow, 5));

        if (kategoriBaru == null || jenisBaru == null || nominalStr == null || tanggalBaru == null) {
            JOptionPane.showMessageDialog(this, "Edit dibatalkan.");
            return;
        }

        double nominalBaru = Double.parseDouble(nominalStr);

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/smartkas", "root", "");
            PreparedStatement ps = conn.prepareStatement(
                "UPDATE laporan SET kategori=?, jenis=?, nominal=?, catatan=?, tanggal=? WHERE id=?")) {

            ps.setString(1, kategoriBaru);
            ps.setString(2, jenisBaru);
            ps.setDouble(3, nominalBaru);
            ps.setString(4, catatanBaru);
            ps.setString(5, tanggalBaru);
            ps.setInt(6, id);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");

            // update di tabel
            model.setValueAt(kategoriBaru, selectedRow, 1);
            model.setValueAt(jenisBaru, selectedRow, 2);
            model.setValueAt(nominalBaru, selectedRow, 3);
            model.setValueAt(catatanBaru, selectedRow, 4);
            model.setValueAt(tanggalBaru, selectedRow, 5);

            hitungTotal();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed


