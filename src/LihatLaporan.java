private void loadData() {
    // 1️⃣ Ambil model dari JTable
    DefaultTableModel model = (DefaultTableModel) TabelLaporan.getModel();
    model.setRowCount(0); // bersihkan tabel sebelum isi ulang

    double totalPemasukan = 0;
    double totalPengeluaran = 0;

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/smartkas", "root", "");
         PreparedStatement ps = conn.prepareStatement("SELECT * FROM laporan");
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            Object[] row = {
                rs.getInt("id"),
                rs.getString("kategori"),
                rs.getString("jenis"),
                rs.getDouble("nominal"),
                rs.getString("catatan"),
                rs.getString("tanggal")
            };
            model.addRow(row);

            if (rs.getString("jenis").equalsIgnoreCase("Pemasukan")) {
                totalPemasukan += rs.getDouble("nominal");
            } else {
                totalPengeluaran += rs.getDouble("nominal");
            }
        }

        // Update label total
        lblPemasukan.setText(String.valueOf(totalPemasukan));
        lblPengeluaran.setText(String.valueOf(totalPengeluaran));
        lblSisa.setText(String.valueOf(totalPemasukan - totalPengeluaran));

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error saat memuat data: " + e.getMessage());
    }
}


// ====== HITUNG TOTAL PEMASUKAN, PENGELUARAN, SISA ======
private void hitungTotal() {
    DefaultTableModel model = (DefaultTableModel) TabelLaporan.getModel();

    double totalPemasukan = 0;
    double totalPengeluaran = 0;

    for (int i = 0; i < model.getRowCount(); i++) {
        String jenis = model.getValueAt(i, 2).toString(); // kolom ke-2 = 'jenis'
        double nominal = Double.parseDouble(model.getValueAt(i, 3).toString()); // kolom ke-3 = 'nominal'

        if (jenis.equalsIgnoreCase("Pemasukan")) {
            totalPemasukan += nominal;
        } else if (jenis.equalsIgnoreCase("Pengeluaran")) {
            totalPengeluaran += nominal;
        }
    }

    lblPemasukan.setText(String.valueOf(totalPemasukan));
    lblPengeluaran.setText(String.valueOf(totalPengeluaran));
    lblSisa.setText(String.valueOf(totalPemasukan - totalPengeluaran));
}


